def introduce(name, age=25, profession="Superhero"):
    print("Hello I am", name)
    print("I am", age, "years old")
    print("I am a", profession)


introduce("Peter Parker", 20, "Photographer")
introduce("Tony Stark", 30)
introduce(profession="Black Widow", name="Natasha Romanoff")

introduce("Thor")


